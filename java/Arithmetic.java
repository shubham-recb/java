public class Arithmetic{
	public static void main(String args[]){
		int a=5,b=2;
		System.out.println("a+b="+(a+b));
		System.out.println("a-b="+(a-b));
		System.out.println("a*b="+(a*b));
		System.out.println("a/b="+(a/b));
		System.out.println("a%b="+(a%b));
		System.out.println("a++="+(a++));
		System.out.println("++a="+(++a));
		System.out.println("a--"+(a--));
		System.out.println("--a"+(--a));
	}
}